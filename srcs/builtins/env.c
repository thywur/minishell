#include "minishell.h"

int	ft_env(char ***env, int fd[2])
{
	int	i;

	i = 0;
	while ((*env)[i])
	{
		write(fd[1], (*env)[i], ft_strlen((*env)[i]));
		write(fd[1], "\n", 1);
		i++;
	}
	return (0);
}
